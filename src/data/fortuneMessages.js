/**
 * Get a fortune message based on the user's 일주 오행.
 * Uses the heavenly stem's element for primary selection.
 */
export function getFortuneMessage(ohaeng) {
  const messages = FORTUNE_MESSAGES[ohaeng] || FORTUNE_MESSAGES['default'];
  return messages[Math.floor(Math.random() * messages.length)];
}

